class Enemy
{
private:
  float x, y;
  int type;
  bool active;

public:
  Enemy(float x, float y, int type)
  {
    this->x = x;
    this->y = y;
    this->active = true;
    this->type = type;
    printf("Enemy created\n");
    printf("x: %f\n", getX());
    printf("y: %f\n", getY());
  }

  void drawSqua(float _x, float _y, float size = 2.5)
  {
    _x = _x * size + x;
    _y = _y * size + y;
    glBegin(GL_QUADS);
    glVertex2f(_x, _y);
    glVertex2f(_x + size, _y);
    glVertex2f(_x + size, _y + size);
    glVertex2f(_x, _y + size);
    glEnd();
  }

  void draw()
  {
    if (active)
    {
      if (type == 0)
      {
        for (int i = 0; i < 19; i++)
        {
          for (int j = 0; j < 30; j++)
          {
            if (vectorEnemy1[i][j] != 0xffacacac)
            {
              glColor3f(((vectorEnemy1[i][j] >> 16) & 0xFF) / 255.0f, ((vectorEnemy1[i][j] >> 8) & 0xFF) / 255.0f, (vectorEnemy1[i][j] & 0xFF) / 255.0f);
              drawSqua(j, 18 - i);
            }
          }
        }
      }
      else
      {
        for (int i = 0; i < 22; i++)
        {
          for (int j = 0; j < 26; j++)
          {
            if (vectorEnemy2[i][j] != 0xffacacac)
            {
              glColor3f(((vectorEnemy2[i][j] >> 16) & 0xFF) / 255.0f, ((vectorEnemy2[i][j] >> 8) & 0xFF) / 255.0f, (vectorEnemy2[i][j] & 0xFF) / 255.0f);
              drawSqua(j, 21 - i);
            }
          }
        }
      }
    }
  }

  void drawExplotion()
  {
    for (int i = 0; i < 14; i++)
    {
      for (int j = 0; j < 15; j++)
      {
        if (vectorExplotion[i][j] == 1)
        {
          glColor3f(1.0f, 0.5f, 0.0f); // Naranja
          drawSqua(j, i);
        }
        else if (vectorExplotion[i][j] == 2)
        {
          glColor3f(1.0f, 0.0f, 0.0f); // Rojo
          drawSqua(j, i);
        }
      }
    }
  }

  void move(float speed, bool &start)
  {
    while (active)
    {
      y -= speed;
      if (y < 0)
      {
        active = false;
        start = false;
      }
      std::this_thread::sleep_for(std::chrono::milliseconds(100));
    }
  }

  bool isActive()
  {
    return active;
  }

  float getX()
  {
    return x;
  }

  float getY()
  {
    return y;
  }

  void setActive(bool active)
  {
    this->active = active;
  }
};